    <p style=""><span style="">是起源于造血干细胞的一组骨髓克隆增生异常的疾病，表现为无效造血、难治性血细胞减少，有转化为</span><span style="">急性髓系白血病</span><span style="">的风险。须经我们认可医院的血液科专科医生根据外周血和骨髓活检明确诊断为骨髓增生异常综合征，并须满足下列全部条件：</span></p>
