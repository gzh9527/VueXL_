    <p style=""><span style="font-weight:bold">未成年人其他类型的类风湿性关节炎不在互助范围内。</span></p>
    <h5 style=" "><span style="font-weight:bold">（七十）</span><span style="font-weight:bold">自体造血干细胞移植术</span></h5>
    <p style=""><span style="">指为治疗造血功能损害或者造血系统恶性肿瘤，已经实际实施了造血干细胞（包括骨髓造血干细胞、外周血造血干细胞和脐血造血干细胞）的自体移植手术。手术须在我们认可医院的专科医生认为是医疗必须的情况下进行。</span></p>
