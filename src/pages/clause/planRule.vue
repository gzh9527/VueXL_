    <p ><span >（</span><span >2）心电图有损伤性的ST</span><span >段改变</span><span >但未出现病理Q波。</span></p>

    <h5 ><span style=" font-weight:bold">(三) 冠状动脉介入手术</span></h5>
    <p ><span >指为治疗明显的冠状动脉狭窄性疾病，首次实际实施了冠状动脉球囊扩张成形术、冠状动脉支架植入术或激光冠状动脉成形术。</span></p>

    <h5 ><span style=" font-weight:bold">(四) 特定脑中风后遗症</span></h5>
    <p ><span >指因脑血管的突发病变引起脑血管出血、栓塞或梗塞，并导致神经系统永久性的功能障碍。神经系统永久性的功能障碍，指疾病确诊</span><span >180天后，仍遗留下列一种或一种以上障碍：</span></p>
    <p ><span >（</span><span >1）</span><span >一</span><span >肢或</span><span >一肢以上</span><span >肢体机能部分丧失，其肢体肌力为Ⅲ级,或小于Ⅲ</span><span >级但尚未</span><span >达到脑中风后遗症的给付标准;</span></p>
    <p ><span >（</span><span >2）自主生活能力部分丧失，无法独立完成六项基本日常生活活动中的一项或两项。</span></p>

    <h5 ><span style=" font-weight:bold">(五) 心脏瓣膜介入手术</span></h5>
    <p ><span >指为治疗心脏瓣膜疾病，实际实施了非开胸的经胸壁打孔内镜手术或经皮经导管介入手术进行的心脏瓣膜置换或修复手术。</span></p>

    <h5 ><span style=" font-weight:bold">(六) 特定面积</span><span style=" font-weight:bold">Ⅲ</span><span style=" font-weight:bold">度烧伤</span></h5>
    <p ><span >指烧伤程度为Ⅲ度，且Ⅲ度烧伤面积为全身体表面积的</span><span >10%或10%以上，但尚未达到20%。体表面积根据《中国新九分法》计算。</span></p>

    <h5 ><span style=" font-weight:bold">(七) 主动脉介入手术</span></h5>
    <p ><span >指为治疗主动脉疾病实际实施了经皮经导管进行的动脉内手术。主动脉指胸主动脉和腹主动脉，但不包括胸主动脉和腹主动脉的分支血管。</span></p>

    <h5 ><span style=" font-weight:bold">(八) 严重脑垂体瘤、脑囊肿、脑动脉瘤及脑血管瘤</span></h5>
    <p ><span >指经头颅断层扫描</span><span >(CT)、核磁共振检查(MRI)或正电子发射断层扫描(PET)等影像学检查被确诊为下列病变，并实际接受了全部或部分的切除手术或已进行了放射治疗。</span></p>
    <p ><span >（</span><span >1）脑垂体瘤;</span></p>
    <p ><span >（</span><span >2）脑囊肿;</span></p>
    <p ><span >（</span><span >3）脑动脉瘤、脑血管瘤。</span></p>

    <h5 ><span style=" font-weight:bold">(九) 视力受损</span></h5>
    <p ><span >指因疾病或意外伤害导致双目视力永久不可逆性丧失，虽然未达到本章程所指重大疾病“</span><span >3周岁后双目失明”的给付标准，但须满足下列全部条件：</span></p>
    <p ><span >（</span><span >1）双眼中较好眼矫正视力低于0.1(采用国际标准视力表，如果使用其它视力表应进行换算);</span></p>
    <p ><span >（</span><span >2）双眼中较好眼视野半径小于20度。</span></p>
    <p ><span >申请互助时年龄必须在三周岁以上，并且提供申请互助当时的视力丧失诊断及检查证据。</span></p>

    <h5 ><span style=" font-weight:bold">(十) 严重头部外伤</span></h5>
    <p ><span >指因头部遭受机械性外力伤害，引起</span><span >脑重要</span><span >部位损伤，并且由头颅断层扫描</span><span >(CT)、核磁共振检查(MRI)或正电子发射断层扫描(PET)等影像学检查证实。头部外伤导致神经系统功能障碍，虽然未达到本章程所指重大疾病“严重脑损伤”的给付标准，但须满足下列条件之一：</span></p>
    <p ><span >（</span><span >1）已接受全麻下颅骨切开颅内血肿清除术(颅骨钻孔术除外)；</span></p>
    <p ><span >（</span><span >2）在遭受外伤180天后</span><span >一</span><span >肢或</span><span >一肢以上</span><span >肢体机能部分丧失，其肢体肌力为Ⅲ级,或小于Ⅲ级。</span></p>

    <h5 ><span style=" font-weight:bold">(十一) 原发性肺动脉高压</span></h5>
    <p ><span >指由于原发性肺动脉高压进行性发展而导致右心室肥厚，已经造成永久不可逆性的体力活动能力受限，达到美国纽约心脏病学会心功能状态分级</span><span >III 级及以上，但尚未达到IV 级,且静息状态下肺动脉平均</span><span >压超过</span><span >25mmHg，但尚未超过30mmHg。</span></p>

    <h5 ><span style=" font-weight:bold">(十二) 单个肢体缺失</span></h5>
    <p ><span >指因疾病或意外伤害导致一个肢体自腕关节或踝关节近端（靠近躯干端）以上完全</span><span > 性断离。</span></p>

    <h5 ><span style=" font-weight:bold">(十三) 人工耳蜗植入术</span></h5>
    <p ><span >指由于耳蜗的永久损害而实际实施了人工耳蜗植入手术。诊断须经专科医师确认在医学上是必要的，且在植入手术之前已经符合下列全部条件：</span></p>
    <p ><span >（</span><span >1）双耳持续</span><span >180天</span><span >以上重度感音神经性耳聋；</span></p>
    <p ><span >（</span><span >2）使用相应的听力辅助设备效果不佳。 </span></p>
    <p ><span >(十四) 慢性肾功能损害–肾功能衰竭期</span></p>
    <p ><span >指会员因双肾功能慢性不可逆性损害达到肾功能衰竭期，诊断必须满足所有以下标准。</span></p>
    <p ><span >（</span><span >1）GFR&lt;25%</span></p>
    <p ><span >（</span><span >2）</span><span >Scr</span><span >&gt;5mg/dl或&gt;442umol/L</span></p>
    <p ><span >（</span><span >3）持续180天</span></p>

    <h5 ><span style=" font-weight:bold">(十五) 川崎病合并冠状动脉扩张</span></h5>
    <p ><span >川崎病指一种原因未明的血管炎综合征。本计划仅对诊断性检查证实川崎病并发冠状动脉次瘤或其他心血管异常，但未接受手术治疗的情况予以赔付。</span></p>

    <h5 ><span style=" font-weight:bold">(十六) 运动神经元病</span></h5>
    <p ><span >是一组中枢神经系统运动神经元的进行性变性疾病，包括进行性脊肌萎缩症、进行性延髓麻痹症、原发性侧索硬化症、肌萎缩性侧索硬化症。须满足自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的二项条件。</span></p>
    <h5 ><span style=" font-weight:bold">（十七）轻型</span><span style=" font-weight:bold">良性脑肿瘤</span></h5>
    <p ><span >指脑的良性肿瘤，未危及生命，无明显的症状和体征，经由头颅断层扫描（</span><span >CT）、核磁共振检查（MRI）或正电子发射断层扫描（PET）等影像学检查证实</span><span >，并</span><span >实际</span><span >进行了</span><span >手术</span><span >、介入或</span><span >放射治疗。</span></p>
