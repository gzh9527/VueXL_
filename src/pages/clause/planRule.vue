    <h5 style=" "><span style="font-weight:bold">（六十</span><span style="font-weight:bold">七</span><span style="font-weight:bold">）</span><span style="font-weight:bold">严重自身免疫性肝炎</span></h5>
    <p style=""><span style="">是一种原因不明的慢性肝脏的坏死性炎性疾病，机体免疫机制被破坏，产生针对肝脏自身抗原的抗体导致自身免疫反应，从而破坏肝细胞造成肝脏炎症坏死，进而发展为肝硬化。必须满足下列全部条件：</span></p>
