    <h5 style=" "><span style="font-weight:bold">（</span><span style="font-weight:bold">六十八</span><span style="font-weight:bold">）</span><span style="font-weight:bold">进行性多灶性白质脑病</span></h5>
    <p style=""><span style="">指一种亚急性脱髓鞘性脑病，常常发现于免疫缺陷的病人。须经我们认可医院的医生根据脑组织活检明确诊断。</span></p>
    <h5 style=" "><span style="font-weight:bold">（</span><span style="font-weight:bold">六十九</span><span style="font-weight:bold">）</span><span style="font-weight:bold">全身型幼年类风湿性关节炎</span></h5>
