    <h5 style=" "><span style="font-weight:bold">（八十）</span><span style="font-weight:bold">严重</span><span style="font-weight:bold">黏</span><span style="font-weight:bold">多糖贮积症</span></h5>
    <p style=""><span style="">指一种进行性多系统受累的溶酶体贮积病，以面容异常、骨骼畸形、肝脾增大、心脏病变等为表现特征。根据酶活性测定或基因突变分析明确诊断，并满足下列全部条件：</span></p>
    <p style=""><span style="">（1）由我们认可的专职合格心理检测工作者根据年龄采用对应的智力量表检测证实智力低常，智力商数（IQ）不高于70 ，心理测验工作者必须持有心理测量专业委员会资格认定书；</span></p>
    <p style=""><span style="">（2）智力低常自确认之日起持续180天以上。</span></p>
    <p style=""><span style="">本项疾病责任不受本计划责任免除中“遗传性疾病，先天性畸形、变形或者染色体异常”的限制。</span></p>
    <h5 style=" "><span style="font-weight:bold">（八十</span><span style="font-weight:bold">一</span><span style="font-weight:bold">）</span><span style="font-weight:bold">严重法布里（Fabry）病</span></h5>
    <p style=""><span style="">指一种罕见的X 连锁遗传性疾病，由于X 染色体长臂中段编码α-半乳糖苷酶A（α-Gal A）的基因突变，导致α-半乳糖苷酶A 结构和功能异常，使其代谢底物三已糖神经酰胺</span><span style="">（</span><span style="">Globotriaosylceramide</span><span style="">，GL-3）和相关鞘糖脂在全身多个器官内大量堆积所导致的临床综合征。根据基因检测明确诊断，并须满足以下任</span><span style="">一</span><span style="">条件：</span></p>
    <p style=""><span style="">（1）中枢神经系统受累，存在短暂性脑缺血发作或缺血性卒中；</span></p>
    <p style=""><span style="">（2）肾脏器官受累，达肾病综合征水平；</span></p>
    <p style=""><span style="">（3）冠状动脉受累导致心肌缺血、心脏瓣膜病变或肥厚性心肌病。</span></p>
    <p style=""><span style="">本项疾病责任不受本计划责任免除中“遗传性疾病，先天性畸形、变形或者染色体异常”的限制。</span></p>
    <h5 style=" "><span style="font-weight:bold">（八十</span><span style="font-weight:bold">二</span><span style="font-weight:bold">）</span><span style="font-weight:bold">严重戈谢病</span></h5>
    <p style=""><span style="">指</span><span style="">一种常染色体隐性遗传的溶酶体贮积病，以葡萄糖脑苷脂在巨噬细胞溶酶体贮积导致多器官受累为表现特征。根据葡萄糖脑苷脂酶活性检测明确诊断，实际实施了脾切除手术或造血干细胞移植术。</span></p>
    <p style=""><span style="">本项疾病责任不受本计划责任免除中“遗传性疾病，先天性畸形、变形或者染色体异常”的限制。</span></p>

    <p style=""><a name="_Hlk491696572"></a></p>
    <p style=""><span style="-aw-bookmark-end:_Hlk491696572"></span><span style="font-weight:bold">上述重度重症、轻度重症释义中，部分术语释义如下：</span></p>
    <p style=""><span style="">（一）肢体机能完全丧失</span></p>
    <p style=""><span style="">肢体机能完全丧失是指肢体的三大关节中的两大关节僵硬，或不能随意识活动。肢体是指包括肩关节的整个上肢或包括髋关节的整个下肢。</span></p>

    <p style=""><span style="">（二）语言能力或咀嚼吞咽能力完全丧失</span></p>
    <p style=""><span style="">语言能力完全丧失是指无法发出四种语音（包括口唇音、齿舌音、口盖音和喉头音）中的任何三种、或声带全部切除，或因大脑语言中枢受伤害而患失语症。</span></p>
    <p style=""><span style="">咀嚼吞咽能力完全丧失是指因牙齿以外的原因导致器质障碍或机能障碍，以致不能作咀嚼吞咽运动，除流质食物外不能摄取或吞咽的状态。</span></p>

    <p style=""><span style="">（三）六项基本日常生活活动</span></p>
    <p style=""><span style="">六项基本日常生活活动是指：</span></p>
    <p style=""><span style="">1．穿衣：自己能够穿衣及脱衣；</span></p>
    <p style=""><span style="">2．移动：自己从一个房间到另一个房间；</span></p>
    <p style=""><span style="">3．行动：自己上下床或上下轮椅；</span></p>
    <p style=""><span style="">4．如厕：自己控制进行大小便；</span></p>
    <p style=""><span style="">5．进食：自己从已准备好的碗或碟中取食物放入口中；</span></p>
    <p style=""><span style="">6．洗澡：自己进行淋浴或盆浴。</span></p>

    <p style=""><span style="">（四）</span><span style="">永久不</span><span style="">可逆</span></p>
    <p style=""><span style="">永久不</span><span style="">可逆是指</span><span style="">自疾病</span><span style="">确诊或意外伤害发生之日起，经过积极治疗</span><span style="">180天后，仍无法通过现有医疗手段恢复。</span></p>

    <p style=""><span style="">（五）三大关节</span></p>
    <p style=""><span style="">上肢三大关节是指肩、肘、腕关节，下肢三大关节是指髋、膝、踝关节。</span></p>
    <h3 ><span style=" font-weight:bold"> </span><span style=" font-weight:bold">第</span><span style="font-weight:bold">7节 释义</span></h3>
    <h4 style=" "><span style=" font-weight:bold">7.1. 周岁</span></h4>
    <p style=""><span style="">指按有效身份证件中记载的出生日期计算的年龄，自出生之日起为零周岁，每经过一年增加一岁，不足一年的不计。</span></p>

    <h4 style=" "><span style=" font-weight:bold">7.2. 有效身份证件</span></h4>
    <p style=""><span style="">指由中华人民共和国政府主管部门规定的能够证明其身份且附有本人照片的证件，如：居民身份证及中华人民共和国政府主管部门颁发或者认可的有效护照或者其他身份证明文件。</span></p>

    <h4 style=" "><span style=" font-weight:bold">7.3 初次发生</span></h4>
    <p style=""><span style="">指会员观察期后首次出现重度重症或轻度重症的前兆或异常的身体状况，包括与重度重症或轻度重症相关的症状及体征。</span></p>

    <h4 style=" "><span style=" font-weight:bold">7.4 医院</span></h4>
    <p style=""><span style="">指国家卫生部医院等级分类中的二级或二级以上的公立医院，但不包括附属于前述医院或单独作为诊所、康复、护理、疗养、戒酒、戒毒等或相类似的医疗机构。同时该医院必须具有符合有关医院管理规定设置标准的医疗设备，并且全日二十四小时有合格医师及</span><span style="">护士驻</span><span style="">院提供医疗和护理等服务。</span></p>
    <p style=""><span style="">会员</span><span style="">罹患本计划</span><span style="">重度重症中（见</span><span style="">6.2</span><span style="">）</span><span style="">约定的特定</span><span style="">的（</span><span style="">七十八</span><span style="">），（</span><span style="">七十九</span><span style="">），（</span><span style="">八十</span><span style="">）（</span><span style="">八十一</span><span style="">），（</span><span style="">八十二</span><span style="">.</span><span style="">）</span><span style="">罕见病的,应由国务院卫生行政主管部门指定的罕见病诊疗协作</span><span style="">网医院</span><span style="">确诊。</span></p>

    <h4 style=" "><span style=" font-weight:bold">7.5 专科医生</span></h4>
    <p style=""><span style="">专科医生应当同时满足以下四项资格条件：</span></p>
    <p style=""><span style="">（</span><span style="">1）具有有效的中华人民共和国《医师资格证书》；</span></p>
    <p style=""><span style="">（</span><span style="">2）具有有效的中华人民共和国《医师执业证书》，并按期到相关部门登记注册；</span></p>
    <p style=""><span style="">（</span><span style="">3）具有有效的中华人民共和国主治医师或主治医师以上职称的《医师职称证书》；</span></p>
    <p style=""><span style="">（</span><span style="">4）在二级或二级以上医院的相应科室从事临床工作三年以上。</span></p>

    <h4 style=" "><span style=" font-weight:bold">7.6 毒品</span></h4>
    <p style=""><span style="">指中华人民共和国刑法规定的鸦片、海洛因、甲基苯丙胺（冰毒）、吗啡、大麻、可卡因以及国家规定管制的其他能够使人形成瘾癖的麻醉药品和精神药品，但不包括由医生开具并</span><span style="">遵</span><span style="">医嘱使用的用于治疗疾病但含有毒品成分的处方药品。</span></p>

    <h4 style=" "><span style=" font-weight:bold">7.7 感染艾滋病病毒或患艾滋病</span></h4>
    <p style=""><span style="">艾滋病病毒指人类免疫缺陷病毒，英文缩写为</span><span style="">HIV。艾滋病指人类免疫缺陷病毒引起的获得性免疫缺陷综合症，英文缩写为AIDS。</span></p>

    <p style=""><span style="">在人体血液或其它样本中检测到艾滋病病毒或其抗体呈阳性，没有出现临床症状或体征的，为感染艾滋病病毒；如果同时出现了明显临床症状或体征的，为患艾滋病。</span></p>

    <h4 style=" "><span style=" font-weight:bold">7.8 遗传性疾病</span></h4>
    <p style=""><span style="">指生殖细胞或受精卵的遗传物质（染色体和基因）发生突变或畸变所引起的疾病，通常具有由亲代传至后代的垂直传递的特征。</span></p>

    <h4 style=" "><span style="font-weight:bold">7.9 先天性畸形、变形或染色体异常</span></h4>
    <p style=""><span style="">指会员出生时就具有的畸形、变形或染色体异常。先天性畸形、变形和染色体异常依照世界卫生组织《疾病和有关健康问题的国际统计分类（</span><span style="">ICD-10）》确定。</span></p>

    <h4 style=" "><span style=" font-weight:bold">7.10 慢性疾病或体征</span></h4>
    <p style=""><span style="">1）动脉硬化、高血压、</span><span style="">严重</span><span style="">静脉炎、动脉炎、</span><span style="">表皮以外的</span><span style="">各类血管瘤、血管畸形、血管闭塞、梗塞或狭窄等影响血管功能的疾病</span></p>
    <p style=""><span style="">2）</span><span style="">冠心病</span><span style="">、心律失常、心内膜炎、肺心病、心肌病、心瓣膜病、风心病、肺心病</span><span style="">、先天性心脏病</span><span style="">等影响心脏功能及心电图改变的疾病</span></p>
