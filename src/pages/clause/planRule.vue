    <p style=""><span style="">4）哮喘、结核、</span><span style="">慢性支气管炎5年以上、慢性阻塞性肺病、</span><span style="">支气管扩张、肺气肿、尘肺、胸积液、肺纤维化、间质性肺病等影响肺功能的疾病</span></p>
    <p style=""><span style="">5）</span><span style="">各类</span><span style="">肝炎</span><span style="">（乙型肝炎、丙型肝炎、自身免疫性肝炎、酒精性肝炎、药物性肝炎）</span><span style="">、肝硬化、肝内结石、食管静脉曲张、</span><span style="">肝炎病毒携带者（</span><span style="">肝大三阳、肝小三阳</span><span style="">）</span><span style="">、萎缩性胃炎、</span><span style="">胃息肉、</span><span style="">消化道溃疡</span><span style="">、胃</span><span style="">出血或穿孔、胆囊炎、胰腺炎、胆结石、化脓性胆管炎、结肠炎、肠结核、肠息肉等</span></p>
