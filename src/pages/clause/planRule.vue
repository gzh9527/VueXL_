    <h5 style=" "><span style="font-weight:bold">（七十</span><span style="font-weight:bold">二</span><span style="font-weight:bold">）</span><span style="font-weight:bold">左室壁瘤切除手术</span></h5>
    <p style=""><span style="">经我们认可医院的医生明确诊断为左室室壁瘤，并且实际实施了开胸打开心脏进行的室壁瘤切除手术。</span></p>
    <h5 style=" "><span style="font-weight:bold">（七十</span><span style="font-weight:bold">三</span><span style="font-weight:bold">）</span><span style="font-weight:bold">严重亚急性硬化性全脑炎</span></h5>
    <p style=""><span style="">指一种以大脑白质和灰质损害为主的全脑炎。是由于缺损型麻疹病毒慢性持续感染所致的一种罕见的致命性中枢神经系统退变性疾病。早期以炎症性病变为主，晚期主要为神经元坏死和胶质增生，核内包涵体是本病的特征性改变之一。疾病确诊180天后，仍遗留下列一种或者一种以上障碍：</span></p>
