    <p style=""><span style="">（</span><span style="">1）机器、器具使用或操作，如电钻、电锤、电锯、电焊、电力维修、制作机器、加工机器、载重机器（2）海上、船上、</span><span style="font-weight:bold">高空</span><span style="">、森林、山区、山地、地下、潜水、水下、隧道坑道、井下作业；（2）陆上油矿开采业作业；（3）4吨以上卡车、液化或气化油罐车、建筑工程车辆驾驶或随车作业；（4）涉及或接触任何危险物作业（化学物质、爆炸物、有毒物质、氦气、氖气等）；（5）高压电或带电作业；（6）曲棍球、橄榄球、摔跤、柔道、空手道、跆拳道、武术、拳击、卡丁车、潜水、水上摩托车、滑雪、马术、特技表演、雪车、滑翔机、汽车摩托车赛车、跳伞、击</span><span style="">剑运动；（</span><span style="">7）铁牛车、 机动三轮车（含农用三轮车）、吊车、直升机驾驶或操作；（8）违反公园管理或国家相关规定接触、</span><span style="">训养</span><span style="">动物；（9）武打、特技、杂技表演</span></p>

    <h4 style=" "><span style="font-weight:bold">7.12 </span><span style="font-weight:bold">涉</span><span style="font-weight:bold">险活动</span></h4>
    <p style=""><span style="">指明知在某种特定的自然条件下有失去生命或使身体受到伤害的危险，而故意使自己置身于其中的行为，如江河漂流、水上运动，未按正规景区规划线路登山，在禁止垂钓地点或无任何保护和防护下进行垂钓、崖边（或高处）活动或运动、徒步穿越沙漠或人迹罕至的原始森林、铁轨周围活动、高压电周围活动等。</span></p>

    <h4 style=" "><span style="font-weight:bold">7.13</span><span style="font-weight:bold"> 高空</span><span style="width:18.75pt; text-indent:0pt; display:inline-block"></span></h4>
    <p style=""><span style="">凡在坠落高度基准面2m以上（含2m）</span><span style="">的即为高空。</span></p>
    <p style=""><span style="">以下情形不属于高空作业：</span></p>
    <p style=""><span style="">日常生活中必要的登高：例如登梯子取东西、挂东西、自家猪棚牛舍维修、自家屋顶紧急制漏（必须紧急）、自家果树摘果子、自家露天阳台（非屋顶）晾晒衣物等。（在屋顶晒谷物、屋顶乘凉或睡觉或晒衣物等、并非在自家日常生活性登高、工作原因的登高、房屋主体修建、电路及燃气修建等不在此情形之内</span><span style="">）。</span></p>

    <h4 style=" "><span style="font-weight:bold">7.14</span><span style="font-weight:bold">美国纽约心脏病学会心功能状态分级</span></h4>
    <p style=""><span style="">美国纽约心脏病学会心功能状态分为四级：</span></p>
    <p style=""><span style="">Ⅰ</span><span style="">级：体力活动不受限，日常活动不引起过度的乏力、呼吸困难或者心悸；</span></p>
    <p style=""><span style="">Ⅱ</span><span style="">级：体力活动轻度受限，休息时无症状，日常活动即可引起乏力、心悸、呼吸困难或者心绞痛；</span></p>
    <p style=""><span style="">Ⅲ</span><span style="">级：体力活动明显受限，休息时无症状，轻于日常的活动即可引起乏力、心悸、呼吸困难或者心绞痛；</span></p>
    <p style=""><span style="">Ⅳ</span><span style="">级：不能从事任何体力活动，休息时亦有充血性心衰或者心绞痛症状，任何体力活动后加重。</span></p>

    <h4 style=" "><span style="font-weight:bold">7.15</span><span style="font-weight:bold">意外伤害</span></h4>
    <p style=""><span style="">指以外来的、突发的、非本意的、非疾病的客观事件为直接且单独原因导致的身体伤害，斗殴、猝死、自杀以及自伤均不属于意外伤害。</span></p>
    <p style=""><span style="">猝死指</span><span style="">表面健康的人因潜在疾病、机能障碍或者其他原因在出现症状后</span><span style="">24</span><span style="">小时内发生的非暴力性突然死亡。猝死的认定以医院的诊断和公安部门的鉴定为准。</span></p>
    <p style=""><span style="">成员因意外伤害情况紧急必须立即就医的，医疗服务机构不受</span><span style="">6.6</span><span style="">列举医院范围的限制。</span></p>

    <h4 style=" "><span style="font-weight:bold">7.16 </span><span style="font-weight:bold">饮</span><span style="font-weight:bold">酒后驾驶</span></h4>
    <p style=""><span style="">指经检测或者鉴定，发生互助事故时车辆驾驶人员有饮</span><span style="">酒情况</span><span style="">。</span></p>

    <h4 style=" "><span style="font-weight:bold">7.17 </span><span style="font-weight:bold">无合法有效驾驶证驾驶</span></h4>
    <p style=""><span style="">指下列情形之一：</span></p>
    <p style=""><span style="">（</span><span style="">1</span><span style="">）没有取得中华人民共和国有关主管部门颁发或者认可的驾驶资格证书；</span></p>
    <p style=""><span style="">（</span><span style="">2</span><span style="">）驾驶与合法有效驾驶证准驾车型不相符合的车辆；</span></p>
    <p style=""><span style="">（</span><span style="">3</span><span style="">）持审验不合格的驾驶证驾驶；</span></p>
    <p style=""><span style="">（</span><span style="">4</span><span style="">）驾驶证已过有效期。</span></p>

    <h4 style=" "><span style="font-weight:bold">7.18 </span><span style="font-weight:bold">无合法有效行驶证</span></h4>
    <p style=""><span style="">指发生互助事故时没有按照公安机关交通管理部门机动车登记制度的规定进行登记并领取机动车行驶证或者临时通行牌证等法定证件。包括下列情形之一：</span></p>
    <p style=""><span style="">（</span><span style="">1</span><span style="">）未办理行驶证或者行驶证在申办过程中；</span></p>
    <p style=""><span style="">（</span><span style="">2</span><span style="">）机动车行驶证被依法注销登记；</span></p>
    <p style=""><span style="">（</span><span style="">3</span><span style="">）未在行驶证检验有效期内依法按时进行或者未通过机动车安全技术检验。</span></p>

    <h4 style=" "><span style="font-weight:bold">7.19 </span><span style="font-weight:bold">机动车</span></h4>
    <p style=""><span style="">指以动力装置驱动或者牵引，</span><span style="">上道路</span><span style="">行驶的</span><span style="">供人员</span><span style="">乘用或者用于运送物品以及进行工程专项作业的轮式车辆。</span></p>

    <h4 style=" "><span style="font-weight:bold">7.20 </span><span style="font-weight:bold">感染艾滋病病毒或者患艾滋病</span></h4>
    <p style=""><span style="">艾滋病病毒指人类免疫缺陷病毒，英文缩写为</span><span style="">HIV</span><span style="">。艾滋病指人类免疫缺陷病毒引起的获得性免疫缺陷综合征，英文缩写为</span><span style="">AIDS</span><span style="">。</span></p>
    <p style=""><span style="">在人体血液或者其他样本中检测到艾滋病病毒或者其抗体呈阳性，没有出现临床症状或者体征的，为感染艾滋病病毒；如果同时出现了明显临床症状或者体征的，为患艾滋病。</span></p>

    <h4 style=" "><span style="font-weight:bold">7</span><span style="font-weight:bold">.21 </span><span style="font-weight:bold">报销</span><span style="font-weight:bold">费用</span><span style="font-weight:bold">范围</span></h4>
    <p style=""><span style="">针对于</span><span style="">互助</span><span style="">类型为报销型的</span><span style="">会员</span><span style="">因确诊和治疗该疾病实际发生的、合理</span><span style="">且必要</span><span style="">的医疗费用，平台对其个人支付的部分进行一次或多次</span><span style="">报销</span><span style="">。</span></p>
    <p style=""><span style="">(</span><span style="">一</span><span style="">) </span><span style="">住院医疗费用</span></p>
    <p style=""><span style="">指互助会员经医院诊断罹患本计划约定范围内的疾病，并接受由具有相应资质的医务人员提供的医学必需的住院医疗，由此互助会员需个人支付的、合理</span><span style="">且必要</span><span style="">的医疗费用，包括：</span></p>
    <p style=""><span style="">(1)</span><span style="">床位费</span></p>
    <p style=""><span style="">(2)</span><span style="">膳食费、护理费</span></p>
    <p style=""><span style="">(3)</span><span style="">重症监护室费</span></p>
    <p style=""><span style="">(4)</span><span style="">检查检验费</span></p>
    <p style=""><span style="">(5)</span><span style="">手术费、麻醉费、药品费、材料费、医疗机构拥有的医疗设备使用费</span></p>
    <p style=""><span style="">(6)</span><span style="">治疗费、医生费、会诊费</span></p>
    <p style=""><span style="">(7)</span><span style="">手术植入器材费</span></p>
    <p style=""><span style="">(8)</span><span style="">陪床费：</span></p>
    <p style=""><span style="">a.</span><span style="">未满</span><span style="">18</span><span style="">周岁互助会员住院期间，其父亲、母亲或其法定监护人陪同住院所发生的加床费，但此费用仅限一人；</span></p>
    <p style=""><span style="">b.</span><span style="">女性互助会员在住院期间，其出生未满一周岁的新生婴儿所发生的住院加床费。</span></p>
    <p style=""><span style="">(9)</span><span style="">视为住院医疗的癌症治疗、肾透析费：因癌症进行诊断、随诊、复查、化疗、放疗而发生的门诊费，肾</span><span style="">透析门</span><span style="">诊费；</span></p>

    <p style=""><span style="">(</span><span style="">二</span><span style="">) </span><span style="">门诊医疗费用</span></p>
    <p style=""><span style="">(1)</span><span style="">特殊门诊医疗费用</span></p>
    <p style=""><span style="">指互助会员在医院接受特殊门诊治疗时，互助会员需个人支付的、合理</span><span style="">且必要</span><span style="">的特殊门诊医疗费用，包括：</span></p>
    <p style=""><span style="">a.</span><span style="">门诊肾透析费；</span></p>
    <p style=""><span style="">b.</span><span style="">门诊恶性肿瘤治疗费，包括化学疗法、放射疗法、肿瘤免疫疗法、肿瘤内分泌疗法、肿瘤靶向疗法等的治疗费用。</span></p>
    <p style=""><span style="">c.</span><span style="">器官移植后的门诊抗排异治疗费。</span></p>
    <p style=""><span style="">(2)</span><span style="">一般门急诊医疗费用</span></p>
    <p style=""><span style="">互助会员经医院诊断罹患本计划约定范围内的疾病后发生的、互助会员需个人支付的、合理</span><span style="">且必要门</span><span style="">急诊医疗费。包括：</span></p>
    <p style=""><span style="">a.</span><span style="">挂号费、诊察费；</span></p>
    <p style=""><span style="">b.</span><span style="">治疗费；</span></p>
    <p style=""><span style="">c.</span><span style="">药品费；</span></p>
    <p style=""><span style="">d.</span><span style="">检查检验费；</span></p>
    <p style=""><span style="">e.</span><span style="">手术费；</span></p>
    <p style=""><span style="">f.</span><span style="">非正式住院的留院观察费用。</span></p>

    <h4 style=" "><span style="font-weight:bold">7</span><span style="font-weight:bold">.22</span><span style="font-weight:bold">合理且必需</span></h4>
    <p style=""><span style="">需满足以下条件：</span></p>
    <p style=""><span style="">（</span><span style="">1）符合通常惯例</span></p>
    <p style=""><span style="">指与接受医疗服务所在地通行治疗规范、通行治疗方法、平均医疗费用价格水平</span><span style=""> </span><span style="">一致的费用。</span></p>
    <p style=""><span style="">对是否符合通常惯例由我们审核人员根据客观、审慎、合理的原则进行审核；如果会员对审核结果有不同意见，可由双方认同的权威医学机构或者权威医学专家进行审核鉴定。</span></p>
    <p style=""><span style="">（</span><span style="">2）医学必需：</span></p>
    <p style=""><span style="">1）指医疗费用符合下列所有条件：</span></p>
    <p style=""><span style="">2）治疗意外伤害或者疾病所必需的项目；</span></p>
    <p style=""><span style="">3）不超过安全、足量治疗原则的项目；</span></p>
    <p style=""><span style="">4）由医生开具的处方药；</span></p>
    <p style=""><span style="">5）非试验性的、非研究性的项目；</span></p>
    <p style=""><span style="">6）与治疗所在地普遍接受的医疗专业实践标准一致的项目。 </span><span style="">对是否医学必需由我们互助审核人员根据客观、审慎、合理的原则进行审核；如果会员对审核结果有不同意见，可由双方认同的权威医学机构或者权威医学专家进行审核鉴定。</span></p>
    <h4 style=" "><span style="font-weight:bold">7.23</span><span style="font-weight:bold">化学疗法</span><span style="font-weight:bold"> </span></h4>
    <p style=""><span style="">指针对于恶性肿瘤的化学治疗。化疗是使用医学界公认的化疗药物以杀死癌细胞、抑制癌</span><span style=""> </span><span style="">细胞生长繁殖为目的而进行的治疗。本计划所指的化疗为被保险人根据医嘱，在医院进行的静脉注射化疗。</span></p>
    <h4 style=" "><span style="font-weight:bold">7.24 放射疗法</span></h4>
    <p style=""><span style="">指针对恶性肿瘤的放射治疗。放疗是使用各种不同能量的射线照射肿瘤组织，以抑制和杀灭癌细胞为目的而进行的治疗。本计划所指的放疗为被保险人根据医嘱，在医院的专门科室进行的放疗。</span></p>
    <h4 style=" "><span style="font-weight:bold">7.25 肿瘤免疫疗法</span></h4>
    <p style=""><span style="">指应用免疫学原理和方法，使用肿瘤免疫治疗药物提高肿瘤细胞的免疫原性和对效应细胞杀伤的敏感性，激发和增强机体抗肿瘤免疫应答，并应用免疫细胞和效应分子输注宿主体内，协同机体免疫系统杀伤肿瘤、抑制肿瘤生长。本计划所指的肿瘤免疫疗法使用的药物需符合法律、法规</span><span style=""> </span><span style="">要求并经过国家食品药品监督管理总局批准用于临床治疗。</span></p>
    <h4 style=" "><span style="font-weight:bold">7.26 肿瘤内分泌疗法 </span></h4>
    <p style=""><span style="">指针对于恶性肿瘤的内分泌疗法，用药物抑制激素生成和激素反应，杀死癌细胞或</span><span style=""> </span><span style="">抑制癌细胞的生长。本计划所指的内分泌疗法使用的药物需符合法律、法规要求并经过国家食品药品</span><span style=""> </span><span style="">监督管理总局批准用于临床治疗。</span></p>
    <h4 style=" "><span style="font-weight:bold">7.27 肿瘤靶向疗法 </span></h4>
    <p style=""><span style="">指在细胞分子水平上，针对已经明确的致癌点来设计相应的靶</span><span style="">向治疗</span><span style="">药物，利用具有</span><span style=""> </span><span style="">一定特异性的载体，将药物或其他杀伤肿瘤细胞的活性物质选择性地运送到肿瘤部位攻击癌细胞的疗</span><span style=""> </span><span style="">法。本计划所指的靶向疗法使用的药物需符合法律、法规要求并经过国家食品药品监督管理总局批准用于临床治疗。</span></p>

