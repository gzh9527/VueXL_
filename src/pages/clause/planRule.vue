    <p style=""><span style="">7）糖尿病、痛风、甲状腺或肾上腺或胸腺等腺体疾病或代谢异常性疾病</span></p>
    <p style=""><span style="">8）风湿、类风湿，多发硬化、硬皮病等免疫性疾病、结缔组织性疾病，</span></p>
