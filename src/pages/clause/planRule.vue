    <h5 ><span style=" font-weight:bold">(六十五) 川崎病</span></h5>
    <p ><span >指一种原因未明的血管炎综合征，临床表现为急性发热，皮肤粘膜病损和淋巴结肿大。本病必须经专科医生明确诊断，且必须由血管造影或超声心动图检查证实，满足下列至少一项条件：</span></p>
