    <h5 ><span style=" font-weight:bold">(</span><span style=" font-weight:bold">一</span><span style=" font-weight:bold">) 特定恶性病变或恶性肿瘤</span></h5>
    <p ><span >指经病理学检查被明确诊断为下列恶性病变，并且接受了相应的治疗。</span></p>
    <p ><span >（</span><span >1） 原位癌；</span></p>
    <p ><span >（</span><span >2） 类癌；</span></p>
    <p ><span >（</span><span >3） 交界恶性肿瘤；</span></p>
    <p ><span >（</span><span >4） 良性肿瘤恶性变；</span></p>
    <p ><span >（</span><span >5） 相当于Binet分期方案A</span><span >期程度</span><span >的慢性淋巴细胞白血病；</span></p>
    <p ><span >（</span><span >6） 相当于Ann Arbor分期方案I</span><span >期程度</span><span >的何杰金氏病；</span></p>
    <p ><span >（</span><span >7） 皮肤癌（不包括恶性黑色素瘤及已发生转移的皮肤癌）；</span></p>
    <p ><span >（</span><span >8） 无转移的甲状腺乳头状癌；</span></p>
    <p ><span >（</span><span >9） TNM分期为T1N0M0期或更轻分期的前列腺癌。</span></p>
    <p ><span >（10）其它低度恶性肿瘤：包含但不限于骨巨细胞</span><span >瘤</span><span >、隆突性皮肤纤维肉</span><span >瘤</span><span >、少支胶质瘤、腺样囊性癌、粘液表皮样癌，以及其它分化程度高（高分化）、部位局限无转移、经医学认定属于预后良好、生存期长、治疗费用低的</span><span >恶性肿瘤</span><span >。</span></p>

    <h5 ><span style=" font-weight:bold">(二) 不典型的急性心肌梗塞</span></h5>
    <p ><span >指被临床诊断为急性心肌梗塞并接受了急性心肌梗塞治疗，虽然未达到本章程所指重大疾病“急性心肌梗塞”的给付标准，但满足下列全部条件：</span></p>
