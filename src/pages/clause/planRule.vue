    <p style=""><span style="">11）乳房溃疡、乳房</span><span style="">导管扩张、</span><span style="">血性溢乳、子宫肌瘤、子宫内膜异位症、子宫颈细胞变异、腺肌症、卵巢囊肿等疾病</span></p>
    <p style=""><span style="">1</span><span style="">2</span><span style="">）非外伤性</span><span style="">出血、</span><span style="">咯血、呕血、便血、血尿、阴道不规则流血、出血点、淤血等</span><span style="">，包块、结节、占位、赘生物等，反复发作的头痛、晕厥、昏迷、胸痛、腹痛、发热等，正存在或反复出现过的胸水、腹水等</span><span style=""> </span></p>
    <p style=""><span style="">13）职业疾病：尘肺、矽肺、石棉肺、各种慢性中毒等</span></p>
    <p style=""><span style="">14）</span><span style="">感染</span><span style="">了</span><span style="">（非输血或职业原因感染）艾滋病病毒、梅毒</span><span style="">，</span><span style="">患</span><span style="">过</span><span style="">精神和行为障碍</span><span style="">性疾病，有过吸毒史</span></p>
