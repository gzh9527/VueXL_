    <p ><span >急、慢性肝脏疾病导致肝功能衰竭，且经化验、检查证实，满足了下列条件中的至少3项：</span></p>
    <p ><span >（</span><span >1）重度黄疸或黄疸迅速加重；</span></p>
    <p ><span >（</span><span >2）肝性脑病；</span></p>
    <p ><span >（</span><span >3）B超或其他影像学检查显示肝脏体积萎缩；</span></p>
    <p ><span >（</span><span >4）肝功能指标进行性恶化。</span></p>
    <p ><span >（5</span><span >）腹水；</span></p>
    <p ><span >（6</span><span >）充血性脾肿大伴脾功能亢进或食管胃底静脉曲张。</span></p>
    <p ><span >因酗酒或药物滥用导致的肝功能衰竭不在互助范围内。</span></p>

    <h4 ><span style=" font-weight:bold">6.2 重度重症</span></h4>
    <h5 ><a name="_Hlk491696544"><span style=" font-weight:bold"> (</span><span style=" font-weight:bold">一</span><span style=" font-weight:bold">) 恶性肿瘤</span></a></h5>
    <p ><span >指恶性细胞不受控制的进行性增长和扩散，浸润和破坏周围正常组织，可以经血管、淋巴管和体腔扩散转移到身体其它部位的疾病。经病理学检查结果明确诊断，临床诊断属于世界卫生组织《疾病和有关健康问题的国际统计分类（</span><span >ICD-10）》的恶性肿瘤范畴。本计划中“</span><span style=" ">轻度</span><span style=" ">重症</span><span >”(</span><span >见6</span><span >.1)所列明的“特定恶性病变或恶性肿瘤”不在此恶性肿瘤范围内。 </span></p>
    <p style=""><span >恶性肿瘤分为两类，对应不同的互助</span><span >额度</span><span >：</span></p>
    <p style=""><span >一类：无转移的以下恶性肿瘤：肾癌、子宫颈癌、子宫体癌、输卵管癌、乳腺癌、前列腺癌、膀胱癌、输尿管癌、黑色素瘤、肠癌。仅有颈部淋巴结转移但无远处淋巴或器官转移的甲状腺癌。</span></p>
    <p ><span >二类：不属于一类的其它恶性肿瘤</span></p>
    <h5 ><span style=" font-weight:bold">(二) 急性心肌梗塞</span></h5>
    <p ><span >指因冠状动脉阻塞导致的相应区域供血不足造成部分心肌坏死。须满足下列至少三项条件：</span></p>
    <p ><span >（</span><span >1）典型临床表现，例如急性胸痛等；</span></p>
    <p ><span >（</span><span >2）新近的心电图改变提示急性心肌梗塞；</span></p>
    <p ><span >（</span><span >3）心肌酶或肌钙蛋白有诊断意义的升高，或</span><span >呈符合</span><span >急性心肌梗塞的动态性变化；</span></p>
    <p ><span >（</span><span >4）发病90天后，经检查证实左心室功能降低，如左心室射血分数低于50%。</span></p>
    <p style=""><span >新近的心电图改变</span><span >是指新出现的典型心电图改变：宽而深的异常Q波、S-T段弓背向上抬高、T波倒置。</span></p>
    <p style=""><span >心肌酶或肌钙蛋白有诊断意义的升高</span><span >是指比正常的最高值升高10倍以上。</span></p>

    <h5 ><span style=" font-weight:bold"> (三) 脑中风后遗症</span></h5>
    <p ><span >指因脑血管的突发病变引起脑血管出血、栓塞或梗塞，并导致神经系统永久性的功能障碍。神经系统永久性的功能障碍，指疾病确诊</span><span >180天后，仍遗留下列一种或一种以上障碍：</span></p>
    <p ><span >（</span><span >1）</span><span >一</span><span >肢或</span><span >一肢以上</span><span >肢体机能完全丧失；</span></p>
    <p ><span >（</span><span >2）语言能力或咀嚼吞咽能力完全丧失；</span></p>
    <p ><span >（</span><span >3）自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上。</span></p>

    <h5 ><span style=" font-weight:bold">(四) 重大器官移植术或造血干细胞移植术</span></h5>
    <p ><span >重大器官移植术，指</span><span >因相应</span><span >器官功能衰竭，已经实施了肾脏、肝脏、心脏或肺脏的异体移植手术。</span></p>
    <p ><span >造血干细胞移植术，指因造血功能损害或造血系统恶性肿瘤，已经实施了造血干细胞（包括骨髓造血干细胞、外周血造血干细胞和脐血造血干细胞）的异体移植手术。</span></p>

    <h5 ><span style=" font-weight:bold">(五) 冠状动脉搭桥术（或称冠状动脉旁路移植术）</span></h5>
    <p ><span >指为治疗严重的冠心病，实际实施了开胸进行的冠状动脉血管旁路移植的手术。</span><span > </span></p>
    <p ><span >冠状动脉支架植入手术、心导管球囊扩张术、激光射频技术及其他非开胸的介入手术、腔镜手术不在互助范围内。</span></p>

    <h5 ><span style=" font-weight:bold">(六) 终末期肾病（或称慢性肾功能衰竭尿毒症期）</span></h5>
    <p ><span >指双肾功能慢性不可逆性衰竭，达到尿毒症期，经诊断后已经进行了至少</span><span >90天的规律性透析治疗或实施了肾脏移植手术。</span></p>

    <h5 ><span style=" font-weight:bold">(七) 多个肢体缺失</span></h5>
    <p ><span >指因疾病或意外伤害导致两个或两个以上肢体自腕关节或踝关节近端（靠近躯干端）以上完全性断离。</span></p>

    <h5 ><span style=" font-weight:bold">(八) 急性或亚急性重症肝炎</span></h5>
    <p ><span >指因肝炎病毒感染引起肝组织弥漫性坏死，导致急性肝功能衰竭，且经血清学或病毒学检查证实，须满足下列全部条件：</span></p>
    <p ><span >（</span><span >1）重度黄疸或黄疸迅速加重；</span></p>
    <p ><span >（</span><span >2）肝性脑病；</span></p>
    <p ><span >（</span><span >3）B超或其他影像学检查显示肝脏体积急速萎缩；</span></p>
    <p ><span >（</span><span >4）肝功能指标进行性恶化。</span></p>

    <h5 ><span style=" font-weight:bold">(九) 良性脑肿瘤</span></h5>
    <p ><span >指脑的良性肿瘤，已经引起颅内压增高，临床表现为视神经乳头水肿、精神症状、癫痫及运动感觉障碍等，并危及生命。须由头颅断层扫描（</span><span >CT）、核磁共振检查（MRI）或正电子发射断层扫描（PET）等影像学检查证实，并须满足下列至少一项条件：</span></p>
    <p ><span >（</span><span >1）实际实施了开颅进行的脑肿瘤完全切除或部分切除的手术；</span></p>
    <p ><span >（</span><span >2）实际实施了对脑肿瘤进行的放射治疗。</span></p>
    <p ><span >脑垂体瘤、脑囊肿、脑动脉瘤及脑血管瘤</span><span >”</span><span >不在此良性脑肿瘤范围内。</span></p>

    <h5 ><span style=" font-weight:bold">(十) 慢性肝功能衰竭失代偿期</span></h5>
    <p ><span >指因慢性肝脏疾病导致肝功能衰竭。须满足下列全部条件：</span></p>
    <p ><span >（</span><span >1）持续性黄疸；</span></p>
    <p ><span >（</span><span >2）腹水；</span></p>
    <p ><span >（</span><span >3）肝性脑病；</span></p>
    <p ><span >（</span><span >4）充血性脾肿大伴脾功能亢进或食管胃底静脉曲张。</span></p>
    <p ><span >因酗酒或药物滥用导致的肝功能衰竭不在互助范围内。</span></p>

    <h5 ><span style=" font-weight:bold">(十一) 脑炎后遗症或脑膜炎后遗症</span></h5>
    <p ><span >指因患脑炎或脑膜炎导致的神经系统永久性的功能障碍。神经系统永久性的功能障碍，指疾病首次确诊</span><span >180天后，仍残留下列一种或一种以上障碍：</span></p>
    <p ><span >（</span><span >1）</span><span >一</span><span >肢或</span><span >一肢以上</span><span >肢体机能完全丧失；</span></p>
    <p ><span >（</span><span >2）语言能力或咀嚼吞咽能力完全丧失；</span></p>
    <p ><span >（</span><span >3）自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上。</span></p>

    <h5 ><span style=" font-weight:bold">(十二) 深度昏迷</span></h5>
    <p ><span >指因疾病或意外伤害导致意识丧失</span><span >,对外界刺激和体内需求均无反应，昏迷程度按照格拉斯哥昏迷分级（Glasgow coma scale）结果为5分或5分以下，且已经持续使用呼吸机及其它生命维持系统96小时以上。</span></p>
    <p ><span >由于酗酒或药物滥用导致的深度昏迷不在互助范围内。</span></p>

    <h5 ><span style=" font-weight:bold">(十三) 双耳失聪</span></h5>
    <p ><span >指因疾病或意外伤害导致双耳听力永久不可逆性丧失，在</span><span >500赫兹、1000赫兹和2000赫兹语音频率下，平均听阈大于90分贝，且经纯音听力测试、声导抗检测或听觉诱发电位检测等证实。</span></p>
    <p ><span >除内耳结构损伤等情形外，会员申请互助时年龄必须在三周岁以上，并且提供申请互助当时的听力丧失诊断及检查证据。</span></p>
    <h5 ><span style=" font-weight:bold"> (十四) 双目失明</span></h5>
    <p ><span >指因疾病或意外伤害导致双目视力永久不可逆性丧失，双眼中</span><span >较好眼须满足</span><span >下列至少一项条件：</span></p>
    <p ><span >（</span><span >1）眼球缺失或摘除；</span></p>
    <p ><span >（</span><span >2）矫正视力低于0.02（采用国际标准视力表，如果使用其他视力表应进行换算）；</span></p>
    <p ><span >（</span><span >3）视野半径小于5度。</span></p>
    <p ><span >除眼球缺失或摘除等情形外，会员申请互助时年龄必须在三周岁以上，并且提供申请互助当时的视力丧失诊断及检查证据。</span></p>
    <h5 ><span style=" font-weight:bold"> (十五) 瘫痪</span></h5>
    <p ><span >指因疾病或意外伤害导致两肢或两</span><span >肢以上</span><span >肢体机能永久完全丧失。肢体机能永久完全丧失，指疾病确诊</span><span >180天后或意外伤害发生180天后，</span><span >每肢三大</span><span >关节中的两大关节仍然完全僵硬，或不能随意识活动。</span></p>

    <h5 ><span style=" font-weight:bold">(十六) </span><span style="font-weight:bold">开胸进行的</span><span style=" font-weight:bold">心脏瓣膜手术</span></h5>
    <p ><span >指为治疗心脏瓣膜疾病，实际实施了开胸进行的心脏瓣膜置换或修复的手术。</span></p>

    <h5 ><span style=" font-weight:bold">(十七) 严重阿尔茨海默病</span></h5>
    <p ><span >指因大脑进行性、不可逆性改变导致智能严重衰退或丧失，临床表现为明显的认知能力障碍、行为异常和社交能力减退，其日常生活必须持续受到他人监护。须由头颅断层扫描（</span><span >CT）、核磁共振检查（MRI）或正电子发射断层扫描（PET）等影像学检查证实，且自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上。</span></p>
    <p ><span >神经官能症和精神疾病不在互助范围内。</span></p>

    <h5 ><span style=" font-weight:bold">(十八) 严重脑损伤</span></h5>
    <p ><span >指因头部遭受机械性外力，引起</span><span >脑重要</span><span >部位损伤，导致神经系统永久性的功能障碍。须由头颅断层扫描（</span><span >CT）、核磁共振检查（MRI）或正电子发射断层扫描（PET）等影像学检查证实。神经系统永久性的功能障碍，指脑损伤180天后，仍遗留下列一种或一种以上障碍：</span></p>
    <p ><span >（</span><span >1）</span><span >一</span><span >肢或</span><span >一肢以上</span><span >肢体机能完全丧失；</span></p>
    <p ><span >（</span><span >2）语言能力或咀嚼吞咽能力完全丧失；</span></p>
    <p ><span >（</span><span >3）自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上。</span></p>

    <h5 ><span style=" font-weight:bold">(十九) 严重帕金森病</span></h5>
    <p ><span >指一种中枢神经系统的退行性疾病，临床表现为震颤麻痹、共济失调等。须满足下列全部条件：</span></p>
    <p ><span >（</span><span >1）药物治疗无法控制病情；</span></p>
    <p ><span >（</span><span >2）自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上。</span></p>
    <p ><span >继</span><span >发性帕金</span><span >森综合征不在互助范围内。</span></p>

    <h5 ><span style=" font-weight:bold">(二十) 严重Ⅲ度烧伤</span></h5>
    <p ><span >指烧伤程度为Ⅲ度，且Ⅲ度烧伤面积达到全身体表面积的</span><span >20％或20％以上。体表面积根据 《中国新九分法》计算。</span></p>

    <h5 ><span style=" font-weight:bold">(二十一) 严重原发性肺动脉高压</span></h5>
    <p ><span >指不明原因的肺动脉压力持续性增高，进行性发展而导致的慢性疾病，已经造成永久不可逆性的体力活动能力受限，达到美国纽约心脏病学会心功能状态分级Ⅳ级，且静息状态下肺动脉平均</span><span >压超过</span><span >30mmHg。</span></p>

    <h5 ><span style=" font-weight:bold">(二十二) 严重运动神经元病</span></h5>
    <p ><span >是一组中枢神经系统运动神经元的进行性变性疾病，包括进行性脊肌萎缩症、进行性延髓麻痹症、原发性侧索硬化症、肌萎缩性侧索硬化症。须满足自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上的条件。</span></p>
    <p style=""><span >&nbsp;</span></p>

    <h5 ><span style=" font-weight:bold">(二十三) 语言能力丧失</span></h5>
    <p ><span >指因疾病或意外伤害导致完全丧失语言能力，经过积极治疗至少360天</span><span >（声带完全切除不受此时间限制），仍无法通过现有医疗手段恢复。</span></p>
    <p ><span >精神心理因素所致的语言能力丧失不在互助范围内。</span><span >申请互助时，会员年齢必须在三周岁以上，并且须提供申请互助当时的语言能力丧失诊断及检查证据。</span></p>

    <h5 ><span style=" font-weight:bold">(二十四) 重型再生障碍性贫血</span></h5>
    <p ><span >指因骨髓造血功能慢性持续性衰竭导致的贫血、中性粒细胞减少及血小板减少。须满足下列全部条件：</span></p>
    <p ><span >（</span><span >1）骨髓穿刺检查或骨髓活检结果支持诊断；</span></p>
    <p ><span >（</span><span >2）外周血象必须具备以下三项条件：</span></p>
    <p ><span >①</span><span > 中性粒细胞绝对值≤0.5&times;109/L；</span></p>
    <p ><span >②</span><span > 网织红细胞＜1%；</span></p>
    <p ><span >③</span><span > 血小板绝对值≤20&times;109/L。</span></p>

    <h5 ><span style=" font-weight:bold">(二十五)主动脉手术</span></h5>
    <p ><span >指为治疗主动脉疾病，实际实施了开胸或开腹进行的切除、置换、修补病损主动脉血管的手术。主动脉指胸主动脉和腹主动脉，不包括胸主动脉和腹主动脉的分支血管。</span><span > </span></p>
    <p ><span >动脉内血管成形术不在互助范围内。</span></p>

    <h5 ><span style=" font-weight:bold"> (二十六) 严重重症肌无力</span></h5>
    <p ><span >指一种神经与肌肉接头部位传递障碍的自身免疫性疾病，临床特征是局部或全身横纹肌于活动时易于疲劳无力，颅神经眼外肌最易累及，也可涉及呼吸肌、下肢近端肌群以至全身肌肉。且须满足下列全部条件：</span></p>
    <p ><span >（</span><span >1）经药物或胸腺手术治疗一年以上无法控制病情，丧失正常工作能力;</span></p>
    <p ><span >（</span><span >2）出现眼睑下垂，或延髓</span><span >肌</span><span >受累引起的构音困难、进食呛咳,或由于肌无力累及延髓肌、呼吸肌而致机体呼吸功能不正常的危急状态即肌无力危象;</span></p>
    <p ><span >（</span><span >3）症状缓解、复发及恶化的交替出现，临床接受新斯的明等抗胆碱酯酶药物治疗的病史。</span></p>

    <h5 ><span style=" font-weight:bold">(二十七) 严重多发性硬化</span></h5>
    <p ><span >多发性硬化为中枢神经系统</span><span >白质多灶性</span><span >脱髓鞘病变，病变有时累及灰质。多发性硬化必须明确诊断，并且已经造成神经系统功能损害导致会员永久不可逆性的无法独立完成下列基本日常生活活动：</span></p>
    <p ><span >（</span><span >1）移动：自己从一个房间移动到另一个房间；</span></p>
    <p ><span >（</span><span >2）进食：自己从已准备好的碗或碟中取食物放入口中。</span></p>

    <h5 ><span style=" font-weight:bold">(二十八) 严重冠心病</span></h5>
    <p ><span >指经心脏科专科医师根据冠状动脉造影检查结果确诊的三支主要血管（左冠状动脉主干和右冠状动脉，或前降支、左旋支和右冠状动脉）严重狭窄性病变（至少一支血管</span><span >管</span><span >腔直径减少</span><span >75％以上和其他两支血管</span><span >管</span><span >腔直径减少60％以上）。前降支、左旋支及右冠状动脉的分支血管的狭窄不作为本互助计划的衡量指标。</span></p>

    <h5 ><span style=" font-weight:bold">(二十九) 严重心肌病</span></h5>
    <p ><span >指会员因心肌病导致慢性心功能损害造成永久不可逆性的心功能衰竭。心功能衰竭程度达到美国纽约心脏病学会的心功能分级标准之心功能Ⅳ级。会员永久不可逆性的体力活动能力受限，不能从事任何体力活动。</span></p>

    <h5 ><span style=" font-weight:bold">(三十) 严重系统性红斑狼疮性肾病－ Ⅲ型或</span><span style=" font-weight:bold">Ⅲ型以上</span><span style=" font-weight:bold">狼疮性肾炎</span></h5>
    <p ><span >系统性红斑狼疮是由多种因素引起，累及多系统的自身免疫性疾病。其特点是生成自身抗体对抗多种自身抗原。多见于育龄妇女。</span></p>
    <p ><span >本计划所指的系统性红斑狼疮仅限于累及肾脏（经肾脏活检确认的，符合</span><span >WHO诊断标准定义Ⅲ型或</span><span >Ⅲ型以上</span><span >狼疮性肾炎）的系统性红斑狼疮。其他类型的红斑性狼疮，如盘状狼疮、仅累及血液及关节的狼疮不在本计划互助范围</span><span >內</span><span >。</span></p>
    <p ><span >本病必须由免疫和风湿科专家医师确诊。</span></p>
    <p ><span >世界卫生组织（</span><span >WHO）狼疮性肾炎分型：</span></p>
    <p ><span >Ⅰ型</span><span > 微小病变型</span></p>
    <p ><span >Ⅱ型</span><span > 系膜病变型</span></p>
    <p ><span >Ⅲ型</span><span > 局灶及节段增生型</span></p>
    <p ><span >Ⅳ型</span><span > 弥漫增生型</span></p>
    <p ><span >Ⅴ型</span><span > 膜型</span></p>
    <p ><span >Ⅵ型</span><span > 肾小球硬化型</span></p>

    <h5 ><span style=" font-weight:bold">(三十一) 经输血导致的人类免疫缺陷病毒感染</span></h5>
    <p ><span >会员感染上人类免疫缺陷病毒（</span><span >HIV）并且满足下列全部条件：</span></p>
    <p ><span >（</span><span >1）在成为互助会员之后，因输血而感染HIV；</span></p>
    <p ><span >（</span><span >2）提供输血治疗的输血中心或医院出具该项输血感染属医疗责任事故的报告，或者法院终审裁定为医疗责任并且不准上诉；</span></p>
    <p ><span >（</span><span >3）受感染的会员不是血友病患者。</span></p>
    <p ><span >在任何治愈艾滋病（</span><span >AIDS）或阻止HIV病毒作用的疗法被发现以后，或当能防止AIDS发生的医疗方法被研究出来以后，本互助计划将不再予以互助。</span></p>
    <p ><span >任何因其他传播方式（包括：性传播或静脉注射毒品）导致的</span><span >HIV感染不在本计划互助范围内。互助平台拥有获得使用会员的所有血液样本的权利和能够对这些样本进行独立检验的权利。</span></p>

    <h5 ><span style=" font-weight:bold">(三十二) 严重类风湿性关节炎</span></h5>
    <p ><span >指广泛</span><span >分布的慢性进行性多关节病变，表现为关节严重变形，侵犯至少三个主要关节</span><span >(腕关节、肘关节、肩关节、踝关节、膝关节、髋关节)或关节组(如手的多个指间、掌指关节，足的多个足趾、跖趾关节等)。类风湿性关节炎须明确诊断，并已达到类风湿性关节炎功能分类IV级的永久不可逆性关节功能障碍，且须满足下列全部条件：</span></p>
    <p ><span >（</span><span >1）晨僵;</span></p>
    <p ><span >（</span><span >2）对称性关节炎;</span></p>
    <p ><span >（</span><span >3）类风湿性皮下结节;</span></p>
    <p ><span >（</span><span >4）类风湿因子滴度升高;</span></p>
    <p ><span >（</span><span >5）X线显示严重的关节(软骨和</span><span >骨)</span><span >破坏和关节畸形。</span></p>

    <h5 ><span style=" font-weight:bold">(三十三) 严重脊髓灰质炎</span></h5>
    <p ><span >脊髓灰质炎是由于脊髓灰质炎病毒感染所致的瘫痪性疾病，临床表现为运动功能损害或呼吸无力。脊髓灰质炎必须明确诊断。本计划仅对脊髓灰质炎造成的神经系统功能损害导致会员</span><span >一</span><span >肢或</span><span >一肢以上</span><span >肢体机能永久完全丧失的情况予以互助。肢体机能永久完全丧失，指疾病确诊</span><span >180天后，</span><span >每肢三大</span><span >关节中的两大关节仍然完全僵硬，或不能随意识活动。</span></p>

    <h5 ><span style=" font-weight:bold">(三十四) 终末期肺病</span></h5>
    <p ><span >经呼吸科专科医生明确诊断为终末期肺病，且须同时满足下列条件：</span></p>
    <p ><span >（</span><span >1）肺功能测试其FEVB1B持续低于0.75升；</span></p>
    <p ><span >（</span><span >2）病人缺氧必须广泛而持续地进行输氧治疗；</span></p>
    <p ><span >（</span><span >3）动脉血气分析氧分压低于55mmHg；</span></p>
    <p ><span >（</span><span >4）出现慢性呼吸功能衰竭。</span></p>
    <p ><span >因职业因素造成的肺部疾病不在互助范围内。</span></p>

    <h5 ><span style=" font-weight:bold">(三十五) 严重克隆病</span></h5>
    <p ><span >指一种肉芽肿性肠炎。且须满足下列所有条件：</span></p>
    <p ><span >（</span><span >1）已经造成瘘管并伴有肠梗阻或肠穿孔；</span></p>
    <p ><span >（</span><span >2）有结肠镜检查和组织病理学检查作为诊断依据。</span></p>

    <h5 ><span style=" font-weight:bold">(三十六) 严重溃疡性结肠炎</span></h5>
    <p ><span >指伴有致命性电解质紊乱的急性暴发性溃疡性结肠炎，病变已经累及全结肠，表现为严重的血便和系统性症状体征，须由病理学检查结果证实，且已经实施了结肠切除或回肠造</span><span >瘘</span><span >术。</span></p>

    <h5 ><span style=" font-weight:bold">(三十七) 持续植物人状态</span></h5>
    <p ><span >指由于严重颅脑外伤、缺氧持续状态、严重的脑炎或者某种神经毒素造成大脑或脑干严重损害，并导致意识丧失和中枢神经系统功能丧失，仅残存植物神经功能的疾病状态，且植物人状态须已持续</span><span >30天以上。</span></p>

    <h5 ><span style=" font-weight:bold">(三十八) 严重胰岛素依赖型糖尿病</span></h5>
    <p ><span >指由于胰岛素分泌绝对不足引起的慢性血糖升高，经血胰岛素测定、血</span><span >c肽测定或尿c肽测定结果证实，且已经持续性的依赖外源性胰岛素维持180天以上;须至少满足下列一个条件：</span></p>
    <p ><span >（</span><span >1）已出现增殖性视网膜病变;</span></p>
    <p ><span >（</span><span >2）须植入心脏起搏器治疗心脏病;</span></p>
    <p ><span >（</span><span >3）因坏疽需切除至少一个脚趾。</span></p>

    <h5 ><span style=" font-weight:bold">(三十九) 急性坏死性胰腺炎开腹手术</span></h5>
    <p ><span >指为治疗急性坏死性胰腺炎，实际实施了开腹进行的坏死组织清除术、病灶切除术或胰腺部分切除术。</span></p>
    <p ><span >为治疗因酒精中毒引起的急性坏死性胰腺炎的开腹手术不在互助范围内。</span></p>

    <h5 ><span style=" font-weight:bold">(四十) 非阿尔茨海默病所致严重痴呆</span></h5>
    <p ><span >指因阿尔茨海默病以外的脑的器质性疾病造成脑功能衰竭导致永久不可逆性严重痴呆，临床表现为明显的认知能力障碍、行为异常和社交能力减退。会员自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上，日常生活必须持续受到他人监护。导致痴呆的疾病必须明确诊断，并且由完整的临床、实验室和影像学检查结果证实。</span></p>
    <p ><span >神经官能症，精神疾病及酒精中毒所致脑损害不在互助范围内。</span></p>

    <h5 ><span style=" font-weight:bold">(四十一) 坏死性筋膜炎</span></h5>
    <p ><span >坏死性筋膜炎的诊断须同时符合下列要求：</span></p>
    <p ><span >(1) 符合坏死性筋膜炎的一般临床表现；</span></p>
    <p ><span >(2) 细菌培养检出致病菌；</span></p>
    <p ><span >(3) 出现广泛性肌肉及软组织坏死，并导致身体受影响部位永久完全失去功能。所谓永久完全失去功能是指受影响部位的功能丧失超过六个月者。</span></p>

    <h5 ><span style=" font-weight:bold">(四十二) 原发性硬化性胆管炎</span></h5>
    <p ><span >指一种胆汁淤积综合征，其特征是肝内、肝外胆道因纤维化性炎症逐渐狭窄，并最终导致完全阻塞而发展为肝硬化。本病须经内镜逆行胰胆管造影等影像学检查证实，并须满足下列全部条件：</span></p>
    <p ><span >（</span><span >1） 总胆红素和直接胆红素同时升高，血清ALP&gt;200U/L；</span></p>
    <p ><span >（</span><span >2） 持续性黄疸病史；</span></p>
    <p ><span >（</span><span >3） 出现胆汁性肝硬化或门脉高压。</span></p>
    <p ><span >因肿瘤或胆管损伤等继发性的硬化性胆管炎不在互助范围内。</span></p>

    <h5 ><span style=" font-weight:bold">(四十三) 肾髓质囊性病</span></h5>
    <p ><span >肾髓质囊性病的诊断须同时符合下列要求：</span></p>
    <p ><span >(1)肾髓质有囊肿、肾小管萎缩及间质纤维化等病理改变；</span></p>
    <p ><span >(2)贫血、多尿及肾功能衰竭等临床表现；</span></p>
    <p ><span >(3)诊断须由肾组织活检确定。</span></p>

    <h5 ><span style=" font-weight:bold">(四十四) 肌营养不良症</span></h5>
    <p ><span >由专科医师确认的诊断为</span><span >Duchenne，Becker，或Limb Girdle 肌营养不良症（所有其他类型的肌营养不良症均不在互助范围内），已进行了至少90 天的治疗，并提供肌肉活检和血CPK 检</span><span >测证实。该疾病</span><span >须导致</span><span >会员自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上，或者导致会员永久性的卧床，没有外部帮助无法起床。</span></p>

    <h5 ><span style=" font-weight:bold">(四十五) 严重哮喘</span></h5>
    <p ><span >会员必须在过去两年内曾发生哮喘持续状态，并满足以下标准中的两项或两项以上标准：</span></p>
    <p ><span >(1)运动耐受力永久并持续地减少并且轻微的运动能引起气促；</span></p>
    <p ><span >(2)长期胸腔过渡膨胀而导致胸腔畸形；</span></p>
    <p ><span >(3)在家及在静息状态下需要吸氧；</span></p>
    <p ><span >(4)持续的每天服用类固醇药物(至少持续</span><span >180天</span><span >以上)。</span></p>

    <h5 ><span style=" font-weight:bold">(四十六) 严重心肌炎</span></h5>
    <p ><span >指心肌局限性或弥漫性的急性或慢性炎症病变，导致心脏功能障碍，达到纽约心脏病学会心功能分级状态分级</span><span >IV 级，且持续至少90 天。</span></p>

    <h5 ><span style=" font-weight:bold">(四十七) Ⅲ度房室传导阻滞</span></h5>
    <p ><span >指心房激动在房室交界区、房室束及其分支内发生阻滞，不能正常地传到</span></p>
    <p ><span >心室的心脏传导性疾病，须满足下列所有条件：</span></p>
    <p ><span >(1)心电图显示房室搏动彼此独立，心室率&lt;50 次/分钟；</span></p>
    <p ><span >(2)出现阿-斯综合征或心力衰竭的表现；</span></p>
    <p ><span >(3)必须持续性依赖心脏起搏器维持心脏正常功能，且已经放置心脏起搏</span></p>
    <p ><span >器。</span></p>

    <h5 ><span style=" font-weight:bold">(四十八) 原发性硬化性胆管炎肝豆状核变性（或称Wilson病）</span></h5>
    <p ><span >指一种常染色体隐性遗传的铜代谢缺陷病，以不同程度的肝细胞损害、脑退行性病变和角膜边缘有铜盐</span><span >沉着环</span><span >为其临床特征，并须满足下列全部条件：</span></p>
    <p ><span >(1)典型症状；</span></p>
    <p ><span >(2)角膜色素环（K-F 环）；</span></p>
    <p ><span >(3)血清铜和血清铜蓝蛋白降低，尿铜增加；</span></p>
    <p ><span >(4)经皮做肝脏活检来定量分析肝脏铜的含量。</span></p>

    <h5 ><span style=" font-weight:bold">(四十九) 因职业关系导致的人类免疫缺陷病毒（HIV）感染</span></h5>
    <p ><span >会员在其常规职业工作过程中遭遇外伤，或者职业需要处理血液或者其他体液时感染上人类免疫缺陷病毒（</span><span >HIV）。须满足下列全部条件：</span></p>
    <p ><span >(1)感染必须是在会员正在从事其职业工作中发生，该职业必须属于以下列表内的职业；</span></p>
    <p ><span >(2)必须提供会员在所报事故发生后的 5 天以内进行的检查报告，该报告必须显示会员血液 HIV 病毒阴性和/或 HIV 抗体阴性；</span></p>
    <p ><span >(3)必须在事故发生后的</span><span >180天</span><span >内证实会员体内存在 HIV 病毒或者HIV 抗体，即血液 HIV 病毒阳性和/或 HIV 抗体阳性。</span></p>
    <p ><span >职业限制如下所示</span><span >:</span></p>
    <p ><span >医生和牙科医生</span></p>
    <p ><span >护士</span></p>
    <p ><span >实验室工作人员</span></p>
    <p ><span >医院护工</span></p>
    <p ><span >医生助理和牙医助理</span></p>
    <p ><span >救护车工作人员</span></p>
    <p ><span >助产士</span></p>
    <p ><span >消防队员</span></p>
    <p ><span >警察</span></p>
    <p ><span >狱警</span></p>

    <h5 ><span style=" font-weight:bold">(五十) 特发性慢性肾上腺皮质功能减退</span></h5>
    <p ><span >指自身免疫性肾上腺炎（既往称：特发性肾上腺皮质萎缩）导致肾上腺萎缩和慢性肾上腺皮质功能减退。须满足下列全部条件：</span></p>
    <p ><span >(1)明确诊断，符合所有以下诊断标准；</span></p>
    <p ><span >①血浆促肾上腺皮质激素</span><span >(ACTH)水平测定大于 100pg/ml；</span></p>
    <p ><span >②血浆和尿游离皮质醇及尿</span><span > 17 羟皮质类固醇、17 酮皮质类固醇测定、血浆肾素活性、血管紧张素 II 和醛固酮测定，显示为原发性肾上腺皮质功能减退症；</span></p>
    <p ><span >③促肾上腺皮质激素</span><span >(ACTH)刺激试验显示为原发性肾上腺皮质功能减退症。</span></p>
    <p ><span >(2)已经采用皮质类固醇替代治疗 180 天以上。肾上腺结核、HIV 感染或艾滋病、感染、肿瘤所致的原发性肾上腺皮质功能减退和</span><span >继发</span><span >性肾上腺皮质功能减退不在互助范围内。</span></p>

    <h5 ><span style=" font-weight:bold">(五十一) 侵蚀性葡萄胎（或称恶性葡萄胎）</span></h5>
    <p ><span >该类疾病是指异常增生的绒毛组织浸润性生长侵入子宫肌层或转移至其他器官或组织的葡萄胎，并已经进行化疗或手术治疗的。</span></p>

    <h5 ><span style=" font-weight:bold">(五十二) 胰腺移植</span></h5>
    <p ><span >指因胰腺功能衰竭，已经实施了在全身麻醉下进行的胰腺的异体器官移植手术（供体必须是人体器官）单纯胰岛移植、部分胰腺组织或细胞的移植不在互助范围内。</span></p>

    <h5 ><span style=" font-weight:bold">(五十三) 埃博拉病毒感染</span></h5>
    <p ><span >指埃博</span><span >拉病毒感染导致的烈性传染病，并须满足下列全部条件：</span></p>
    <p ><span >(1)实验室检查证实埃博拉病毒的存在；</span></p>
    <p ><span >(2)从发病开始有超过 30 天的进行性感染症状。</span></p>

    <h5 ><span style=" font-weight:bold">(五十四) 破裂</span><span style=" font-weight:bold">脑动脉瘤夹闭</span><span style=" font-weight:bold">手术</span></h5>
    <p ><span >指因脑动脉瘤破裂造成蛛网膜下腔出血，会员实际接受了在全麻下进行的开颅动脉瘤夹闭手术。脑动脉瘤（未破裂）预防性手术、颅骨打孔手术、动脉瘤栓塞手术、血管内手术及其他颅脑手术不在互助范围内。</span></p>

    <h5 ><span style=" font-weight:bold">(五十五) 丝虫病所致象皮肿</span></h5>
    <p ><span >指因丝虫感染导致淋巴阻塞性出现严重淋巴水肿，达到国际淋巴学会淋巴水肿分期第</span><span > III 期，临床表现为肢体象皮肿，患肢较</span><span >健肢增粗</span><span >30%以上，日常生活不能自理。</span></p>

    <h5 ><span style=" font-weight:bold">(五十六) 嗜铬细胞瘤</span></h5>
