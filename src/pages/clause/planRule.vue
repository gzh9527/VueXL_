    <h5 style=" "><span style="font-weight:bold">（七十</span><span style="font-weight:bold">四</span><span style="font-weight:bold">）</span><span style="font-weight:bold">严重</span><span style="font-weight:bold">Ⅰ</span><span style="font-weight:bold">型糖尿病</span></h5>
