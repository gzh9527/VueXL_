    <p ><span >（</span><span >1</span><span >）没有取得中华人民共和国有关主管部门颁发或者认可的驾驶资格证书；</span></p>
    <p ><span >（</span><span >2</span><span >）驾驶与合法有效驾驶证准驾车型不相符合的车辆；</span></p>
    <p ><span >（</span><span >3</span><span >）持审验不合格的驾驶证驾驶；</span></p>
    <p ><span >（</span><span >4</span><span >）驾驶证已过有效期。</span></p>

    <h4 ><span style="font-weight:bold">6.12</span><span style="font-weight:bold"> </span><span style="font-weight:bold"> </span><span style="font-weight:bold">无合法有效行驶证</span></h4>
    <p ><span >指发生互助事故时没有按照公安机关交通管理部门机动车登记制度的规定进行登记并领取机动车行驶证或者临时通行牌证等法定证件。包括下列情形之一：</span></p>
    <p ><span >（</span><span >1</span><span >）未办理行驶证或者行驶证在申办过程中；</span></p>
    <p ><span >（</span><span >2</span><span >）机动车行驶证被依法注销登记；</span></p>
    <p ><span >（</span><span >3</span><span >）未在行驶证检验有效期内依法按时进行或者未通过机动车安全技术检验。</span></p>

    <h4 ><span style="font-weight:bold">6.13</span><span style="font-weight:bold"> </span><span style="font-weight:bold"> </span><span style="font-weight:bold"> </span><span style="font-weight:bold">机动车</span></h4>
    <p ><span >指以动力装置驱动或者牵引，上道路行驶的供人员乘用或者用于运送物品以及进行工程专项作业的轮式车辆。</span></p>

    <h4 ><span style="font-weight:bold">6.14</span><span style="font-weight:bold"> 饮酒</span></h4>
    <p ><span >以医疗机构、交通部门、司法部门、个人出具的相关资料为依据。</span></p>

    <h4 ><span style="font-weight:bold">6.15</span><span style="font-weight:bold"> 潜水</span></h4>
    <p ><span >指使用辅助呼吸器材在江、河、湖、海、水库、运河等水域进行的水下运动。</span></p>

    <h4 ><span style="font-weight:bold">6.16</span><span style="font-weight:bold"> 攀岩</span></h4>
    <p ><span >指攀登悬崖、楼宇外墙、人造悬崖、冰崖、冰山等运动。</span></p>

    <h4 ><span style="font-weight:bold">6.17</span><span style="font-weight:bold"> 涉险活动</span></h4>
    <p ><span >指明知在某种特定的自然条件下有失去生命或使身体受到伤害的危险，而故意使自己置身于其中的行为，如江河漂流，水上运动，未按正规景区规划线路登山，在禁止垂钓地点或无任何保护和防护下进行垂钓，崖边（或高处）活动或运动，徒步穿越沙漠或人迹罕至的原始森林，铁轨周围活动，高压电周围活动等。</span></p>

    <h4 ><span style="font-weight:bold">6.18</span><span style="font-weight:bold"> </span><span style="font-weight:bold">武术比赛</span></h4>
    <p ><span >指两人或两人以上对抗柔道、空手道、跆拳道、散打、拳击等各种拳术及各种使用器械的对抗性比赛。</span></p>

    <h4 ><span style="font-weight:bold">6.19</span><span style="font-weight:bold"> 特技表演</span></h4>
    <p ><span >指进行马术、杂技、驯兽等表演。</span></p>

    <h4 ><span style="font-weight:bold">6.20</span><span style="font-weight:bold"> 医疗事故</span></h4>
    <p ><span >指医疗机构及其医务人员在医疗活动中，违反医疗卫生管理法律、行政法规、部门规章和诊疗护理规范、常规，过失造成患者人身损害的事故，必须经国家认可的、有权负责医疗事故技术鉴定工作的医学会组织鉴定后，方可确认医疗事故的成立。</span></p>

    <h4 ><span style="font-weight:bold">6.21</span><span style="font-weight:bold"> 有效身份证件</span></h4>
    <p ><span >指由政府主管部门规定的证明其身份的证件，如：居民身份证、按规定可使用的有效护照、军官证、警官证、士兵证等证件。</span></p>
    <h4 ><span style="font-weight:bold">6</span><span style="font-weight:bold">.22</span><span style="font-weight:bold"> </span><span style="font-weight:bold">见义勇为</span></h4>
    <p ><span >指行为发生地县级以上公安机关、民政部门或法律法规规定的见义勇为确认机构出具证明认定的见义勇为行为。</span></p>

