<h3>
	第<span>6</span>节<span>&nbsp;&nbsp;&nbsp; </span>释义<span></span>
</h3>
<h4>
	<span>6.1.&nbsp;&nbsp;&nbsp; </span>周岁<span></span>
</h4>
<p >
	指按有效身份证件中记载的出生日期计算的年龄，自出生之日起为零周岁，每经过一年增加一岁，不足一年的不计。<span></span>
</p>
<h4>
	<span>6.2.&nbsp; </span>有效身份证件<span></span>
</h4>
<p class="a0">
	指由中华人民共和国政府主管部门规定的能够证明其身份且附有本人照片的证件，如：居民身份证及中华人民共和国政府主管部门颁发或者认可的有效护照或者其他身份证明文件。<span></span>
</p>
<h4>
	<span>6.3&nbsp;&nbsp;&nbsp; </span>医院<span></span>
</h4>
<p >
	指国家卫生部医院等级分类中的二级或二级以上的公立医院或此病的定点医院，但不包括附属于前述医院或单独作为诊所、康复、护理、疗养、戒酒、戒毒等或相类似的医疗机构。<span></span>
</p>
<h4>
	<span>6.4&nbsp;&nbsp; &nbsp;</span>新型冠状病毒<span></span>
</h4>
<p >
	<b>新型冠状病毒：即<span>2019</span>新型冠状病毒，即<span>“2019-nCoV”</span>，因<span>2019</span>年武汉病毒性肺炎病例而被发现，<span>2020</span>年<span>1</span>月<span>12</span>日被世界卫生组织命名。<span></span></b>
</p>
<h3>
	<span>6.5 </span>医护人员<span></span>
</h3>
<p >
	<b>是指有医师或护师证书并与所在医院建立了试用或正式劳动关系的医院工作人员。<span></span></b>
</p>
<h3>
	<span>6.6 </span>一般人员<span></span>
</h3>
<p >
	除医护人员外的会员均为一般人员。<span></span>
</p>
<p class="strong">6.7疑似新型冠状病毒（2019-nCoV）感染；</p>
<p >已经就医并被医生确定为疑似新型冠状病毒（2019-nCoV）感染，正在进一步的观察或正在进一步的确诊中.</p>
<p >或者没有正规就医，但已经符合以下 2 条：</p>
<p >1. 流行病学史</p>
<p >发病前两周内有武汉市旅行史或居住史；或发病前 14 天内曾经接触过来自武汉的发热伴有呼吸道症状的患者，或有聚集性发病。</p>
<p >2. 临床表现</p>
<p >（1）发热；</p>
<p >（2）具有上述肺炎影像学特征；</p>
<p >（3）发病早期白细胞总数正常或降低，或淋巴细胞计数减少。</p>
<p class="strong">6.8新型冠状病毒（2019-nCoV）感染</p>
<p >是指在符合疑似病例标准的基础上，痰液、咽拭子、下呼吸道分泌物等标本行实时荧光RT-PCR检测新型冠状病毒核酸阳性；或病毒基因测序,与已知的新型冠状病毒高度同源。或由其它新的技术或方法检测确诊为该病毒感染。并由二级或二级以上公立医院、或定点医院出具了明确的诊断。</p>

