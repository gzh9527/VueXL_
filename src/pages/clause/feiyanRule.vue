</h4>
<p >
	1. 会员加入本计划前须认真阅读加入条件，自觉遵守，符合加入条件的才可加入。<span></span>
</p>
<p >
	2. 会员提交互助申请时平台将针对当初会员的加入条件进行复核，不符合加入条件的不能申请互助，同时会员资格终止。<span></span>
</p>
<p >
	3. 对于自会员加入本计划之日起至申请互助日期间已分摊的互助金原则上不予退还。经审核确认为非主观恶意的，可以酌情进行退还已支出的互助款项；退还金额由全体会员分摊。<span></span>
</p>
<h4>
	<span>5.2.&nbsp;&nbsp;&nbsp; </span>骗取互助金<span></span>
</h4>
<p >
	互助会员如有伪造材料骗取互助金的行为，新浪互助平台有权解除会员资格；如已经为其发起互助，则新浪互助平台有权通过合法手段追回互助金退还给其他互助会员。<span></span>
</p>
<h4>
	<span>5.3.&nbsp;&nbsp;&nbsp; </span>争议处理<span></span>
</h4>
<p >
	对于是否符合互助条件或是否给付互助金产生争议时，可由会员组成的评审团进行评审。对评审结果依然有异议的可依法向人民法院提起诉讼。<span></span>
</p>
<h4>
	<span>5.4.&nbsp;&nbsp;&nbsp; </span>其它<span></span>
</h4>
